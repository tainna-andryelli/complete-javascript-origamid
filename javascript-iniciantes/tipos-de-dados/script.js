// typeof - returns the data type
var professor = "André";
console.log(typeof professor); // -> string
var time = null;
console.log(typeof time); // -> object
var symbol = Symbol();
console.log(typeof symbol); // -> symbol

// concatenate words
var namee = "José",
  lastName = "Ribeiro";
console.log("Author: " + nomee + " " + lastName);

// Number + string = string
var goals = 11;
var phrase1 = "Luana scored " + goals + " goals";

// Template String - variables or expressions in ${}
var phrase2 = `Luana scored ${gols * 2} goals`;
